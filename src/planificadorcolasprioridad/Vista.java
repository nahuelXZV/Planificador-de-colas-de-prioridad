    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        //CREAR PLANIFICADOR 
        String text = this.ncolas.getText();        //CAMPO CANTIDAD DE COLAS

        if (text.isEmpty()) {                       //VERIFICA CAMPOS VACIOS
            JOptionPane.showMessageDialog(null, "Rellene el campo Cantidad de colas");
            return;
        }
        if (Integer.parseInt(text, 10) < 1 || Integer.parseInt(text, 10) > 8) {     //VALIDA NO SE PASE DE COLAS
            JOptionPane.showMessageDialog(null, "Maximo 8 colas");
            return;
        }
        if (text.matches("[+-]?\\d*(\\.\\d+)?")) {           //VALIDA QUE SEA NUMERO Y NO ESTE VACIO
            n = Integer.parseInt(text);     //GUARDAMOS EL NUMERO DE COLAS
            crearLista();                   //INICIALIZA LAS LISTAS DE QXP Y QXC
            tableEdit();                    //PONE LOS DATOS EN LA TABLA DE ESTADO
            isComplet = true;               //LISTO PARA GUARDARSE
        } else if (!text.matches("[+-]?\\d*(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(null, "Rellene el campo Cantidad de colas con numeros");
        }
    }//GEN-LAST:event_jButton5MouseClicked

    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        //EDITAR LOS QXP Y QXC DE LAS COLAS
        String priori = this.prioridad.getText();
        String Tqxp = this.qxp.getText();
        String Tqxc = this.qxc.getText();

        if (n == -1) {                                                                  //VERIFICA QUE ESTE CREADA LA COLA
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad de colas y cree las colas");
            return;
        }
        if (priori.isEmpty() || Tqxp.isEmpty() || Tqxc.isEmpty()) {                     //VERIFICA QUE NO ESTEN VACIOS LOS CAMPOS
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
            return;
        }

        if (Integer.parseInt(priori, 10) < 1 || Integer.parseInt(priori, 10) > n) {     //VERFICIA QUE EL INDICE SEA CORRECTO
            JOptionPane.showMessageDialog(null, "Ingrese datos correctos");
            return;
        }

        // Es Numero
        boolean b1 = priori.matches("[+-]?\\d*(\\.\\d+)?");
        boolean b2 = Tqxp.matches("[+-]?\\d*(\\.\\d+)?");
        boolean b3 = Tqxc.matches("[+-]?\\d*(\\.\\d+)?");

        if (b1 && b2 && b3) {
            int prio = Integer.parseInt(priori, 10);        //LA PRIORIDAD DE LA COLA
            Lqxp[prio] = Integer.parseInt(Tqxp, 10);        //MODIFICAMOS EL QXP
            Lqxc[prio] = Integer.parseInt(Tqxc, 10);        //MODIFICAMOS EL QXC
            tableEdit();                                    //REFRESCAMOS LA TABLA DE ESTADO
        } else if (!b1 || !b2 || !b3) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos con numeros");
        }
    }//GEN-LAST:event_jButton7MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        //GUARDAR EL PLANIFICADOR
        if (isComplet) {
            timer.cancel();                              //PARAMOS LE PLANIFICADOR
            p.setText("");                               //VACIAMOS LOS CAMPOS
            p1.setText("");
            p2.setText("");
            p3.setText("");
            plan = new Planificador(n, Lqxp, Lqxc);     //CREAMOS UN NUEVO PLANIFICADOR
            resetTable();                               //RESETEAMOS LA RAM
            editable();                                 //CERRAMOS EL AREA DE EDICION Y RESETEAMOS TODO
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad de colas y cree las colas");
        }
    }//GEN-LAST:event_jButton4MouseClicked

