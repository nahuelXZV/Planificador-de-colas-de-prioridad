    private void editable() {
        //ABRE Y CIERRA EL AREA DE EDICION
        if (!this.jPanel2.isVisible()) {
            this.jPanel2.setVisible(true);
            this.setSize(1025, 640);
        } else {
            this.jPanel2.setVisible(false);
            this.setSize(1025, 375);
            this.qxp.setText("");       //LIMPIAMOS LOS CAMPOS
            this.qxc.setText("");
            this.ncolas.setText("");
            this.prioridad.setText("");
            n = -1;
            modelo = new DefaultTableModel();
            Tedit.setModel(modelo);
            isComplet = false;
        }
    }

