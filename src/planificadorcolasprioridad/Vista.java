    //RECARGAR RAM
    static void resetTable() {
        //Encabezado RAM
        modelo = new DefaultTableModel();
        for (String header1 : header) {
            modelo.addColumn(header1);
        }
        ram.setModel(modelo);

        //Procesos
        for (int i = 1; i <= plan.Q.size() - 1; i++) {
            String[] info = plan.getQ(i);
            modelo.addRow(info);
        }
    }

    //INICIAR PLANIFICADOR
    public void start() {
        timer = new Timer();
        timer.scheduleAtFixedRate(new RepeatedTask(), 4000, 4500);
    }

    //DETENER PLANIFICADOR
    public void stop() {
        plan = new Planificador();                  //CREAMOS UN NUEVO PLANIFICADOR
        resetTable();                               //RESETEAMOS LA RAM
        p.setText("");                              //VACIAMOS LOS CAMPOS
        p1.setText("");
        p2.setText("");
        p3.setText("");
        timer.cancel();
    }

    //CREAR LISTAS DE QUANTUMS
    private void crearLista() {
        Lqxp = new int[n + 1];
        Lqxc = new int[n + 1];
        for (int i = 1; i <= n; i++) {
            Lqxp[i] = 1;
            Lqxc[i] = 1;
        }
    }

    //RECARGA TODO LA INTERFAZ
    static class RepeatedTask extends TimerTask {

        public void run() {
            pcb = plan.plan();      //OPTENEMOS EL PCB DEL PLANIFICADOR
            resetTable();           //RECARGAMOS LA RAM
            p.setText(pcb.PID);     //MODIFICAMOS EL PRUN 
            p1.setText(Integer.toString(plan.getQXC()));    //MODIFICAMOS EL QXC
            p2.setText(Integer.toString(plan.getQXP()));    //MODIFICAMOS EL QXP
            p3.setText(plan.Letra[pcb.prioridad]);          //MODIFICAMOS LA COLA
        }
    }

    //TABLA DE PERSONALIZACION
    public void tableEdit() {
        //Encabezado RAM
        modelo = new DefaultTableModel();
        modelo.addColumn("Nro");
        modelo.addColumn("Cola");
        modelo.addColumn("Quantum X cola");
        modelo.addColumn("Quantum X proceso");
        Tedit.setModel(modelo);
        //COLAS
        String[] info = new String[4];
        for (int i = 1; i <= n; i++) {
            info[0] = "" + i;           //INDICE
            info[1] = plan.Letra[i];    //NOMBRE COLA
            info[2] = "" + Lqxc[i];     //CANTIDAD QXC
            info[3] = "" + Lqxp[i];     //CANTIDAD QXP
            modelo.addRow(info);
        }
    }

